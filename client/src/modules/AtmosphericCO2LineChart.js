import { useState } from "react";
import { LineChart, Line, XAxis, YAxis, Tooltip, Legend } from "recharts";

export default function AtmosphericCO2LineChart() {
  const [showMonthlyData, setShowMonthlyData] = useState(false);

  function handleClick() {
    if (showMonthlyData) {
      //if is showing monthly data hide it
      setShowMonthlyData(false);
    } else {
      //if not showing monthly data show it
      setShowMonthlyData(true);
    }
  }
  return (
    <div>
      <LineChart
        margin={{ top: 20, right: 20, left: 20, bottom: 20 }}
        width={800}
        height={400}
      >
        <Line
        xAxisId="annual" 
          data={data}
          hide={showMonthlyData}
          type="monotone"
          dataKey="mean"
          name="CO2 Concentration annual"
          dot={false}
        />
        <Line
        xAxisId="monthly"
          data={data2}
          hide={!showMonthlyData}
          type="monotone"
          dataKey="average"
          name="CO2 Concentration monthly"
          dot={false}
          activeDot={true}
        />
        <XAxis xAxisId="annual" hide={showMonthlyData} dataKey="year" allowDuplicatedCategory={false} />
        <XAxis xAxisId="monthly" hide={!showMonthlyData} dataKey="year_month" />
        <YAxis type="number" domain={["auto", "auto"]} />
        <Tooltip />
        <Legend />
      </LineChart>
      <button onClick={handleClick}>Show monthly data</button>
    </div>
  );
}

